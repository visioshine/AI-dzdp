-- 7. 视图与查询优化
-- ==========================================

-- 商家详细信息视图 (包含评分统计)
CREATE VIEW public.view_merchant_details AS
SELECT 
    m.*,
    COUNT(r.id) as total_reviews,
    COALESCE(AVG(r.rating), 0) as calculated_avg_rating
FROM public.merchants m
LEFT JOIN public.reviews r ON m.id = r.merchant_id AND r.deleted_at IS NULL
WHERE m.deleted_at IS NULL
GROUP BY m.id;

-- ==========================================
-- 8. 安全策略 (RLS)
-- ==========================================

ALTER TABLE public.users ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can view their own profile" ON public.users FOR SELECT USING (auth.uid() = id);
CREATE POLICY "Users can update their own profile" ON public.users FOR UPDATE USING (auth.uid() = id);

ALTER TABLE public.merchants ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Anyone can view active merchants" ON public.merchants FOR SELECT USING (status = 'active' AND deleted_at IS NULL);

ALTER TABLE public.reviews ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Anyone can view reviews" ON public.reviews FOR SELECT USING (deleted_at IS NULL);
CREATE POLICY "Authenticated users can insert reviews" ON public.reviews FOR INSERT WITH CHECK (auth.role() = 'authenticated');
CREATE POLICY "Users can update their own reviews" ON public.reviews FOR UPDATE USING (auth.uid() = user_id);

ALTER TABLE public.favorites ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can view their own favorites" ON public.favorites FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can add favorites" ON public.favorites FOR INSERT WITH CHECK (auth.role() = 'authenticated');
CREATE POLICY "Users can delete their own favorites" ON public.favorites FOR DELETE USING (auth.uid() = user_id);
