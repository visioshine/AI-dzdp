-- 2. 用户系统模块 (Users & RBAC)
-- ==========================================

-- 用户基础表 (与 Supabase Auth 同步)
CREATE TABLE public.users (
    id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
    username TEXT UNIQUE NOT NULL,
    email TEXT UNIQUE NOT NULL,
    phone TEXT,
    avatar_url TEXT,
    bio TEXT, -- 个人简介
    catchphrase TEXT, -- 口头禅
    gender TEXT CHECK (gender IN ('male', 'female', 'secret')), -- 性别
    last_location GEOGRAPHY(POINT), -- 用户最后已知位置
    status TEXT DEFAULT 'active' CHECK (status IN ('active', 'suspended', 'deleted')),
    registered_at TIMESTAMPTZ DEFAULT NOW(),
    last_login_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    deleted_at TIMESTAMPTZ -- 软删除支持
);

COMMENT ON TABLE public.users IS '用户基础信息表';
COMMENT ON COLUMN public.users.status IS '账户状态：active-正常, suspended-封禁, deleted-已注销';

-- 角色表
CREATE TABLE public.roles (
    id SERIAL PRIMARY KEY,
    role_name TEXT UNIQUE NOT NULL, -- e.g., 'admin', 'merchant', 'user'
    description TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 用户角色关联表
CREATE TABLE public.user_roles (
    user_id UUID REFERENCES public.users(id) ON DELETE CASCADE,
    role_id INTEGER REFERENCES public.roles(id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, role_id)
);

-- ==========================================
-- 3. 商家模块 (Merchants)
-- ==========================================

-- 商家主表
CREATE TABLE public.merchants (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    description TEXT, -- 商家描述
    license_no TEXT UNIQUE, -- 营业执照号
    contact_name TEXT,
    phone TEXT, -- 联系电话 (原 contact_phone)
    address TEXT NOT NULL,
    location GEOGRAPHY(POINT), -- 经纬度坐标
    business_hours JSONB, -- 营业时间 e.g., {"open": "09:00", "close": "22:00"}
    category TEXT NOT NULL, -- 商家类型
    rating FLOAT DEFAULT 0,
    reviews_count INTEGER DEFAULT 0, -- 评价数量
    image_url TEXT, -- 商家封面图
    price TEXT, -- 人均消费
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'active', 'closed', 'rejected')),
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    deleted_at TIMESTAMPTZ
);

CREATE INDEX idx_merchants_location ON public.merchants USING GIST (location);
CREATE INDEX idx_merchants_category ON public.merchants(category);

-- 按距离搜索商家的 RPC 函数
CREATE OR REPLACE FUNCTION get_merchants_by_distance(user_lat FLOAT, user_lng FLOAT)
RETURNS TABLE (
    id BIGINT,
    name TEXT,
    description TEXT,
    address TEXT,
    category TEXT,
    phone TEXT,
    rating FLOAT,
    reviews_count INTEGER,
    image_url TEXT,
    price TEXT,
    status TEXT,
    dist_meters FLOAT
) 
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        m.id, m.name, m.description, m.address, m.category, m.phone, 
        m.rating, m.reviews_count, m.image_url, m.price, m.status,
        ST_Distance(m.location, ST_GeographyFromText('POINT(' || user_lng || ' ' || user_lat || ')')) AS dist_meters
    FROM public.merchants m
    WHERE m.status = 'active' AND m.deleted_at IS NULL
    ORDER BY dist_meters ASC;
END;
$$;

